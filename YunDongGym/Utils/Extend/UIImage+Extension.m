#define MAX_IMAGEPIX_WIDTH 800.0f          // max pix 400.0px
#define MAX_IMAGEPIX_HEIGHT 600.0f          // max pix 400.0px
#define MAX_IMAGEDATA_LEN 150000.0f   // max data length 150K

CGFloat DegreesToRadians(CGFloat degrees);
